typedef struct Token{
	string text,tipo;
	bool reservado;
}token;

struct node{
	token tk;
	vector<int> adj;
};

token criaToken(string text, string tipo, bool reservado){
	token novo;
	novo.text = text;
	novo.tipo = tipo;
	novo.reservado = reservado;
	return novo;
}

vector<node> nodes;
int raiz;

void getVar(int root){
	//printf("ainda n deu merda \n");
	for(int x : nodes[root].adj){
		if(nodes[x].tk.tipo=="PAR"){
			if(context.count(nodes[nodes[x].adj[1]].tk.text)){
				redecVar = true;
			}
			if(functContext.count(nodes[nodes[x].adj[1]].tk.text)){
				varFunctConflict=true;		
			}
			context.insert(nodes[nodes[x].adj[1]].tk.text);				
		}
		getVar(x);
	}
}

void dfs_fun(int root){
	for(int x: nodes[root].adj){
		if(nodes[x].tk.tipo=="FUNC_CALL"){
			if(!functContext.count(nodes[nodes[x].adj[0]].tk.text)){
				functNotDeclared=true;			
			}
			if(nodes[x].adj.size()>3){
				dfs_fun(nodes[x].adj[2]);
			}
		}
		else if(nodes[x].tk.tipo == "IDI"){
				if(context.count(nodes[nodes[x].adj[0]].tk.text)){
					redecVar = true;				
				}
				if(functContext.count(nodes[nodes[x].adj[0]].tk.text)){
					varFunctConflict = true;
				}
				for(int i=1;i<nodes[x].adj.size();i++){
					dfs_fun(nodes[x].adj[i]);					
				}				
				context.insert(nodes[nodes[x].adj[0]].tk.text);
								
		}
		else{
			 if(nodes[x].tk.tipo == "id"){
				if(stopper)stopper=0;
				else{
					if(!context.count(nodes[x].tk.text)){
						varNotDeclared = true;
						//cout<<endl<<"Funcao que estamos: "<<nomedafuncao<<endl;	
						//cout<<endl<<nodes[x].tk.text<<endl;
						//for(auto s : context) cout<<endl<<s<<endl;			
					}	
				}		
			}
			dfs_fun(x);		
		}
	}	
}

void dfs(int root){
	//printf("Contador: %d\n",contador++);
	for(int x : nodes[root].adj){
		if(nodes[x].tk.tipo=="FUNCT_SC"){
			context.clear();
			//printf("Funcao sem parametro\n");
			int nome = nodes[x].adj[1];
			if(functContext.count(nodes[nome].tk.text)){
				redecFunct = true;	
			}
			else{
				functContext.insert(nodes[nome].tk.text);
			}
			stopper=1;
			nomedafuncao = nodes[nome].tk.text;
			dfs_fun(x);
		}
		else if (nodes[x].tk.tipo=="FUNCT_CC"){
			context.clear();
			//printf("Funcao com parametro\n");
			int nome = nodes[x].adj[1];
			//printf("%s \n",nodes[nome].tk.text.c_str());
			if(functContext.count(nodes[nome].tk.text)){
				redecFunct = true;	
			}
			else{
				functContext.insert(nodes[nome].tk.text);
			}
			stopper=1;
			nomedafuncao = nodes[nome].tk.text;
			getVar(nodes[x].adj[3]);
			dfs_fun(x);
		}
		else if(nodes[x].tk.tipo=="MAIN"){
			//printf("Main\n");
			nomedafuncao = "main";
			context.clear();
			stopper=0;
			dfs_fun(x);
		}
		else{
			dfs(x);		
		}
	}
}

